     */
    public function index()
    {
        $array = [];
        $flowers = Flower::all();
        foreach ($flowers as $flower) {
            $amount = 0;
            $stocks = WarehouseFlower::where('flower_id', $flower->id)->get();
            foreach ($stocks as $stock) {
                $amount += $stock->amount;
            }
            if ($amount == 0) {
                $array = Arr::add($array, "$flower->id", "$flower->name");
            }
        }
//        dd($array);

        return view('dashboard', compact('array'));
    }

